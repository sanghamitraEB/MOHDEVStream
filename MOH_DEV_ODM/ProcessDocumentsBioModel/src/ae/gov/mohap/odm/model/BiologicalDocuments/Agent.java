package ae.gov.mohap.odm.model.BiologicalDocuments;

import java.util.Date;

public class Agent {
	
	private String licenseNo;
	private String type;
	private String name;
	private String licenseAuthority;
	private Date licenseExpireDate;
	private String emirate;
	private String address;
	private  String poBox;
	private String telephone;
	private String fax;
	private String webSite;
	/**
	 * @return the licenseNo
	 */
	public String getLicenseNo() {
		return licenseNo;
	}
	/**
	 * @param licenseNo the licenseNo to set
	 */
	public void setLicenseNo(String licenseNo) {
		this.licenseNo = licenseNo;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the licenseAuthority
	 */
	public String getLicenseAuthority() {
		return licenseAuthority;
	}
	/**
	 * @param licenseAuthority the licenseAuthority to set
	 */
	public void setLicenseAuthority(String licenseAuthority) {
		this.licenseAuthority = licenseAuthority;
	}
	/**
	 * @return the licenseExpireDate
	 */
	public Date getLicenseExpireDate() {
		return licenseExpireDate;
	}
	/**
	 * @param licenseExpireDate the licenseExpireDate to set
	 */
	public void setLicenseExpireDate(Date licenseExpireDate) {
		this.licenseExpireDate = licenseExpireDate;
	}
	/**
	 * @return the emirate
	 */
	public String getEmirate() {
		return emirate;
	}
	/**
	 * @param emirate the emirate to set
	 */
	public void setEmirate(String emirate) {
		this.emirate = emirate;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	
	/**
	 * @return the poBox
	 */
	public String getPoBox() {
		return poBox;
	}
	/**
	 * @param poBox the poBox to set
	 */
	public void setPoBox(String poBox) {
		this.poBox = poBox;
	}
	/**
	 * @return the telephone
	 */
	public String getTelephone() {
		return telephone;
	}
	/**
	 * @param telephone the telephone to set
	 */
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	/**
	 * @return the fax
	 */
	public String getFax() {
		return fax;
	}
	/**
	 * @param fax the fax to set
	 */
	public void setFax(String fax) {
		this.fax = fax;
	}
	/**
	 * @return the webSite
	 */
	public String getWebSite() {
		return webSite;
	}
	/**
	 * @param webSite the webSite to set
	 */
	public void setWebSite(String webSite) {
		this.webSite = webSite;
	}
	
	

}
